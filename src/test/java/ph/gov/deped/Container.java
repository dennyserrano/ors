package ph.gov.deped;

public class Container {
	
		String left;
		String right;
		public Container(String left, String right) {
			super();
			this.left = left;
			this.right = right;
		}
		public String getLeft() {
			return left;
		}
		public void setLeft(String left) {
			this.left = left;
		}
		public String getRight() {
			return right;
		}
		public void setRight(String right) {
			this.right = right;
		}
		
	
}
